public class PracticeLoop {
	public static void main(String[] args) {
		int counter = 1;
		int limit 	= 5;
		
		while ( counter <= limit) {
			System.out.println("Hello");
			counter = counter + 1;
		}
		
		int x;
		
		for(int i = 0; i < 5; ++i) {
			System.out.println("Hi");
		}
		
		for (x=0 ;x < 5; x++) {
			System.out.println("Haha");
		}
		System.out.println(x);
		
		
	}
}
